title: 按钮组
---

你可以使用<LyButtonGroup></LyButtonGroup>来设置一组按钮， 按钮组下的按钮type和size受控于按钮组的属性

</docs>
<template>
  <LyButtonGroup size="large">
    <LyButton>button1</LyButton>
    <LyButton>button2</LyButton>
    <LyButton>button3</LyButton>
    <LyButton>button4</LyButton>
  </LyButtonGroup>
  <LyButtonGroup type="primary" style="margin-top: 20px">
    <LyButton>button1</LyButton>
    <LyButton>button2</LyButton>
    <LyButton>button3</LyButton>
    <LyButton>button4</LyButton>
  </LyButtonGroup>
  <LyButtonGroup size="small" type="success" style="margin-top: 20px">
    <LyButton>button1</LyButton>
    <LyButton>button2</LyButton>
    <LyButton>button3</LyButton>
    <LyButton>button4</LyButton>
  </LyButtonGroup>
</template>
